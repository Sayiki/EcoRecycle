namespace KapasitasVendingMachine
{
    public class Kapasitas
    {
        public static int totalKapasitas()
        {
            int max = 6;
            int total = 0;
            if (total <= max)
            {
                total = total + 1;
            }
            else
            {
                total = max;
            }
            return total;
        }
    }
}